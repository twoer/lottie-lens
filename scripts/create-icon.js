#!/usr/bin/env node

const fs = require('fs')
const path = require('path')

// 创建一个简单的 PNG 图标占位符 (1x1 像素透明 PNG)
const pngData = Buffer.from([
  0x89,
  0x50,
  0x4e,
  0x47,
  0x0d,
  0x0a,
  0x1a,
  0x0a, // PNG signature
  0x00,
  0x00,
  0x00,
  0x0d, // IHDR chunk length
  0x49,
  0x48,
  0x44,
  0x52, // IHDR
  0x00,
  0x00,
  0x00,
  0x01, // width: 1
  0x00,
  0x00,
  0x00,
  0x01, // height: 1
  0x08,
  0x06,
  0x00,
  0x00,
  0x00, // bit depth, color type, compression, filter, interlace
  0x1f,
  0x15,
  0xc4,
  0x89, // CRC
  0x00,
  0x00,
  0x00,
  0x0a, // IDAT chunk length
  0x49,
  0x44,
  0x41,
  0x54, // IDAT
  0x78,
  0x9c,
  0x63,
  0x00,
  0x01,
  0x00,
  0x00,
  0x05,
  0x00,
  0x01, // compressed data
  0x0d,
  0x0a,
  0x2d,
  0xb4, // CRC
  0x00,
  0x00,
  0x00,
  0x00, // IEND chunk length
  0x49,
  0x45,
  0x4e,
  0x44, // IEND
  0xae,
  0x42,
  0x60,
  0x82, // CRC
])

const iconPath = path.join(__dirname, '../public/icon.png')

try {
  fs.writeFileSync(iconPath, pngData)
  console.log('✅ 临时图标已创建: public/icon.png')
  console.log('💡 建议替换为 512x512px 的正式图标')
} catch (error) {
  console.error('❌ 创建图标失败:', error.message)
  console.log('💡 请手动创建 public/icon.png 文件')
}
